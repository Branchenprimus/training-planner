function interpolate(template: string, params: Record<string, string | number>) {
  return Object.entries(params).reduce(
    (value, [key, replacement]) => value.replaceAll(`{${key}}`, String(replacement)),
    template
  )
}

export function useAppI18n() {
  const locale = useState<Locale>('app-locale', () => 'en')
  const initialized = useState<boolean>('app-locale-initialized', () => false)

  if (import.meta.client && !initialized.value) {
    initialized.value = true
    const stored = window.localStorage.getItem('training-planner-locale')
    if (stored === 'en' || stored === 'de') {
      locale.value = stored
    }
  }

  function setLocale(nextLocale: Locale) {
    locale.value = nextLocale
    if (import.meta.client) {
      window.localStorage.setItem('training-planner-locale', nextLocale)
    }
  }

  function t(key: string, params: Record<string, string | number> = {}) {
    const value = translations[locale.value][key] ?? translations.en[key] ?? key
    return typeof value === 'function' ? value(params) : interpolate(value, params)
  }

  return {
    locale: readonly(locale),
    setLocale,
    t
  }
}
